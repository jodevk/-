def get_input_data():
    """
    Запрашивает у пользователя исходные данные для расчётов.
    Возвращает словарь с данными.
    """
    print("=== Ввод исходных данных для пельменного цеха ===")
    
    Q_sut = float(input("Суточная выработка готовой продукции (т): "))
    t = float(input("Продолжительность рабочей смены (ч): "))
    a_t = float(input("Массовая доля теста в готовой продукции (%): "))
    p_pa = float(input("Производительность пельменного автомата (т/ч): "))
    p_tm = float(input("Производительность тестомесильной машины (т/ч): "))
    p_k = float(input("Производительность куттера (т/ч): "))
    
    return {
        "Q_sut": Q_sut,
        "t": t,
        "a_t": a_t,
        "p_pa": p_pa,
        "p_tm": p_tm,
        "p_k": p_k
    }